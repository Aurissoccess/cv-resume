document.addEventListener('DOMContentLoaded', () => {
    // Current language tracking
    let currentLang = 'uz';

    // Typewriter effect function
    function runTypewriter(element, text, speed = 70, callback = null) {
        element.innerHTML = "";
        let i = 0;
        const interval = setInterval(() => {
            if (i < text.length) {
                element.append(text.charAt(i));
                i++;
            } else {
                clearInterval(interval);
                if (callback) callback();
            }
        }, speed);
    }

    // Translate page elements
    function setLanguage(lang) {
        currentLang = lang;
        document.querySelectorAll('[data-i18n]').forEach(element => {
            const key = element.getAttribute('data-i18n');
            if (translations[lang][key]) {
                if (element.classList.contains('welcome-text')) {
                    // Trigger typewriter effect specifically for welcome text when switched
                    runTypewriter(element, translations[lang][key], 50);
                } else {
                    element.innerHTML = translations[lang][key];
                }
            }
        });
        
        // Update language button text with flag image
        const flagCodes = { uz: 'uz', en: 'us', ru: 'ru' };
        const label = lang.toUpperCase();
        document.querySelector('.lang-btn').innerHTML = `<img src="https://flagcdn.com/w40/${flagCodes[lang]}.png" alt="${label}" class="flag-icon"> ${label} <i class="fa-solid fa-chevron-down"></i>`;
    }

    // Language Dropdown Interactivity
    const langBtn = document.querySelector('.lang-btn');
    const langDropdown = document.querySelector('.lang-dropdown');

    if (langBtn && langDropdown) {
        langBtn.addEventListener('click', (e) => {
            e.stopPropagation();
            langDropdown.classList.toggle('show');
        });

        document.addEventListener('click', () => {
            langDropdown.classList.remove('show');
        });

        document.querySelectorAll('.lang-dropdown button').forEach(button => {
            button.addEventListener('click', (e) => {
                const selectedLang = e.target.getAttribute('data-lang');
                setLanguage(selectedLang);
                langDropdown.classList.remove('show');
            });
        });
    }

    // Run welcome text typewriter on initial load using the default language
    const welcomeEl = document.querySelector('.welcome-text');
    if (welcomeEl) {
        const welcomeKey = welcomeEl.getAttribute('data-i18n') || 'welcome';
        const initialText = translations[currentLang][welcomeKey] || welcomeEl.innerText;
        runTypewriter(welcomeEl, initialText, 60, () => {
            // After welcome is typed, animate the profile box in
            const profileBox = document.querySelector('.hero-profile-box');
            if (profileBox) {
                profileBox.style.opacity = '1';
                profileBox.style.transform = 'translateY(0)';
            }
        });
    }

    // Intersection Observer for scroll animations
    const observerOptions = {
        root: null,
        rootMargin: '0px',
        threshold: 0.15
    };
 
    const observer = new IntersectionObserver((entries, observer) => {
        entries.forEach(entry => {
            if (entry.isIntersecting) {
                entry.target.classList.add('show');
                if (entry.target.id === 'about') {
                    const imgContainer = document.querySelector('.about-image-container');
                    if (imgContainer) {
                        imgContainer.classList.add('show-image');
                    }
                }
                observer.unobserve(entry.target);
            }
        });
    }, observerOptions);
 
    const hiddenElements = document.querySelectorAll('.hidden');
    hiddenElements.forEach((el) => observer.observe(el));

    // Smooth scrolling for navigation links
    document.querySelectorAll('a[href^="#"]').forEach(anchor => {
        anchor.addEventListener('click', function (e) {
            e.preventDefault();
            const targetId = this.getAttribute('href');
            if(targetId === '#') return;
            const targetElement = document.querySelector(targetId);
            if(targetElement) {
                targetElement.scrollIntoView({
                    behavior: 'smooth',
                    block: 'start'
                });
            }
        });
    });

    // Background interactive grid and gold glow effect
    const bgGlow = document.getElementById('bg-glow');
    if (bgGlow) {
        window.addEventListener('mousemove', (e) => {
            document.documentElement.style.setProperty('--mouse-x', `${e.clientX}px`);
            document.documentElement.style.setProperty('--mouse-y', `${e.clientY}px`);
        });

        window.addEventListener('scroll', () => {
            if (window.scrollY > window.innerHeight * 0.4) {
                bgGlow.style.opacity = '1';
            } else {
                bgGlow.style.opacity = '0';
            }
        });
    }
});
